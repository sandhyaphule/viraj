package Polymorphism;
//Polymorphism= method declaration + method defination and implimention
public class Test1 
{
	//compile time--->methodoverloading
	
	//method overloading declaring multiple method + same methodname but with differnt +arugment/parameter is 
	//called methodoverloading.
	public static void main(String[] args)//method declaration
	{
		System.out.println("Good Morning");//method defination and implimention
	}
	
	public static void m1()//method declaration
	{
		System.out.println("Hi");//method defination and implimention
	}
	public static void m2()//method declaration
	{
		System.out.println("Hello");//method defination and implimention
	}

}
